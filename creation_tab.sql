CREATE TABLE Vehicles (
    Make VARCHAR(50),
    Model VARCHAR(50),
    Type_ VARCHAR(50),
    Origin VARCHAR(50),
    DriveTrain VARCHAR(50),
    MSRP DECIMAL(10, 2),
    Invoice DECIMAL(10, 2),
    EngineSize DECIMAL(5, 2),
    Cylinders INT,
    Horsepower INT,
    MPG_City INT,
    MPG_Highway INT,
    Weight_ INT,
    Wheelbase INT,
    Length INT
);